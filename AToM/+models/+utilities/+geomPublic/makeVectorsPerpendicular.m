% remains the same and the second one is rotated in the plane defined by the
% vectors so that they are perpendicular.
%
%  INPUTS
%   vect1: 3D vector, double [1 x 3]
%   vect2: 3D vector, double [1 x 3]
%
%  OUTPUTS
%   vect2: 3D vector, double [1 x 3]
%
%  SYNTAX
%
%  vect2 = models.utilities.geomPublic.makeVectorsPerpendicular(vect1, vect2)
%
% Function makeVectorsPerpendicular forces two vectors to be perpendicular by
% rotating the second vector in plane defined by the vectors.
