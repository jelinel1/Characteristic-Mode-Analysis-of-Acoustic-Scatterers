%% findObject find GroupObject in Group by name
% This method founds GroupObject stored in Group using objects name.
%
%  INPUTS
%   obj: Group object
%   name: name of searched GroupObject, char [1 x N]
%
%  OUTPUTS
%   foundObj: reference to found GroupObject, GroupObject [1 x 1]
%
%  SYNTAX
%
%  foundObj = obj.findObject(name)
%
% The object with name _name_ is searched in the whole Group object _obj_. Found
% object is stored in _foundObj_.
%
% Included in AToM, info@antennatoolbox.com
