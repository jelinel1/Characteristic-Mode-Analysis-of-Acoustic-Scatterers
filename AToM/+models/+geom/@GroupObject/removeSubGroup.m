%% removeSubGroup remove object from GroupObject
% This method removes any GroupObject from a superior GroupObject.
%
%  INPUTS
%   obj: GroupObject [1 x 1]
%   subGroup: GroupObject [1 x 1]
%   sel: selector of GroupObjects to be removed, logical [1 x N]
%
%  SYNTAX
%
%  obj.removeSubGroup(GroupObject, sel)
%
% The GroupObject _subGroup_ is removed from GroupObject _obj_. Input variable
% _sel_ specifies position of subGroup to be removed.
