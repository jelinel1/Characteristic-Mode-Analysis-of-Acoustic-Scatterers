%% getFocalPoints compute focal points of an ellipse
% This function computes position of focal points for defined Ellipse or
% EllipseArc.
%
%  INPUTS
%   obj: EllipseArc object
%
%  OUTPUTS
%   fP: focalPoints, double [2 x 3]
%
%  SYNTAX
%
%  fP = obj.getFocalPoints
%
% Two focal points fP (double [2 x 3]) are computed for object of type
% EllipseArc or Ellipse.
%
