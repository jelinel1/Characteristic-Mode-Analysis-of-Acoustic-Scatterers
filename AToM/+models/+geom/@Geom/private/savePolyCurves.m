%% savePolyCurves save PolyCurves from Geom to HDF file
% This method prepares a space in HDF file and saves PolyCurves from Geom to
% the HDF file.
%
%  INPUTS
%   obj: Geom object [1 x 1]
%   objectID: ID of HDF object where PolyCurves should be saved, double [1 x 1]
%
%  SYNTAX
%
%  obj.savePolyCurves(objectID)
%
% All PolyCurves from Geom (_obj_) are saved to HDF file to object specified by
% _objectID_.
