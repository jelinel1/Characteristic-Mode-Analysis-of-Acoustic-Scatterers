%% getProject returns project name or project order in AToM
% This function returns project name if input is double (project order) or
% project order if input is string (project name).
%
%  INPUTS
%   input: project name, string [1 x N]
%          project order, double [1 x 1]
%
%  SYNTAX
%
%  output = getProject(input)
%
% For 'input' as double (project order), the function returns project name.
% On the other side, for 'input' as string (project name), the function returns
% project order.
%
