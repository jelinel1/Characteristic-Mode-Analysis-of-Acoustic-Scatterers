%% isProjectOpened is possible to find out if project is opened
% This function finds out if input 'projectName' is name of opened project.
%
%  INPUTS
%   projectName: project name, string [1 x N]
%
%  OUTPUTS
%   isOpened: true/false, logical [1 x 1]
%
%  SYNTAX
%
%  isOpened = isProjectOpened(projectName)
%
% Output 'isOpened' is logical true, if input 'projectName' is name of opened
% project. In other case output 'isOpened' is logical false.
%
